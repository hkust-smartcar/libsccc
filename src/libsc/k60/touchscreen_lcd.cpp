bool TouchScreenLcd::Scan(uint8_t mode) {
	uint8_t buf[4];
	uint8_t i = 0;
	bool res = false;
	uint8_t temp;
	uint8_t tempsta;
	static uint8_t t = 0;		//,CPU
	t++;
	if ((t % 10) == 0 || t < 10)		//,10CTP_Scan1,CPU
			{
		GT9147_RD_Reg(GT_GSTID_REG, &mode, 1);	//
		if ((mode & 0X80) && ((mode & 0XF) < 6)) {
			temp = 0;
			GT9147_WR_Reg(GT_GSTID_REG, &temp, 1);	//
		}
		if ((mode & 0XF) && ((mode & 0XF) < 6)) {
			temp = 0XFF << (mode & 0XF);		//1,tp_dev.sta
			tempsta = touch_status;			//tp_dev.sta
			touch_status = (~temp) | TP_PRES_DOWN | TP_CATH_PRES;
			touch_x[4] = touch_x[0];	//0
			touch_y[4] = touch_y[0];
			for (i = 0; i < 5; i++) {
				if (touch_status & (1 << i))	//?
						{
					GT9147_RD_Reg(GT9147_TPX_TBL[i], buf, 4);	//XY
					if (dir)	//
					{
						touch_y[i] = ((uint16_t) buf[1] << 8) + buf[0];
						touch_x[i] = 800 - (((uint16_t) buf[3] << 8) + buf[2]);
					} else {
						touch_x[i] = ((uint16_t) buf[1] << 8) + buf[0];
						touch_y[i] = ((uint16_t) buf[3] << 8) + buf[2];
					}
				}
			}
			res = true;
			if (touch_x[0] > width || touch_y[0] > height)	//()
					{
				if ((mode & 0XF) > 1)		//,.
						{
					touch_x[0] = touch_x[1];
					touch_y[0] = touch_y[1];
					t = 0;				//,10,
				} else					//,()
				{
					touch_x[0] = touch_x[4];
					touch_y[0] = touch_y[4];
					mode = 0X80;
					touch_status = tempsta;	//tp_dev.sta
				}
			} else
				t = 0;					//,10,
		}
	}
	if ((mode & 0X8F) == 0X80)					//
			{
		if (touch_status & TP_PRES_DOWN)	//
		{
			touch_status &= ~(1 << 7);	//
		} else						//
		{
			touch_x[0] = 0xffff;
			touch_y[0] = 0xffff;
			touch_status &= 0XE0;	//
		}
	}
	if (t > 240)
		t = 10;	//10
	return res;
}

