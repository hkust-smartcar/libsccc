void TouchScreenLcd::Clear(uint16_t color) {
	uint32_t index = 0;
	uint32_t totalpoint = width;
	totalpoint *= height;
	SetCursor(0x00, 0x0000);
	LCD_CMD_ADDR = ramcmd;
	for (index = 0; index < totalpoint; index++) {
		LCD_DATA_ADDR = color;
	}
}

void TouchScreenLcd::Fill(int16_t sx, int16_t sy, int16_t ex, int16_t ey, uint16_t color) {
	if (sx < left_bound)
		sx = left_bound;
	if (sy < upper_bound)
		sy = upper_bound;
	if (sx >= width)
		sx = width - 1;
	if (sy >= height)
		sy = height - 1;
	if (ey - sy > 16) {
		uint16_t width = ex - sx;
		uint16_t height = ey - sy;
		Set_Window(sx, sy, width, height);
		uint16_t len = width * height;
		LCD_CMD_ADDR = ramcmd;
		for (uint16_t i = 0; i < len; i++) {
			LCD_DATA_ADDR = color;
		}
		Set_Window(0, 0, this->width, this->height);
	} else {
		uint16_t xlen = ex - sx + 1;
		for (uint16_t i = sy; i <= ey; i++) {
			SetCursor(sx, i);
			LCD_CMD_ADDR = ramcmd;
			for (uint16_t j = 0; j < xlen; j++) {
				LCD_DATA_ADDR = color;
			}
		}
	}
}

void TouchScreenLcd::Fill_Color_Buffer(int16_t sx, int16_t sy, int16_t ex, int16_t ey, const uint16_t *color) {
	if (sx < left_bound)
		sx = left_bound;
	if (sy < upper_bound)
		sy = upper_bound;
	if (sx >= width)
		sx = width - 1;
	if (sy >= height)
		sy = height - 1;
	if (ey - sy > 16) {
		uint16_t width = ex - sx;
		uint16_t height = ey - sy;
		Set_Window(sx, sy, width, height);
		uint16_t len = width * height;
		LCD_CMD_ADDR = ramcmd;
		for (uint16_t i = 0; i < len; i++) {
			LCD_DATA_ADDR = color[i];
		}
		Set_Window(0, 0, this->width, this->height);
	} else {
		uint16_t width = ex - sx + 1;
		uint16_t height = ey - sy + 1;
		for (uint16_t i = 0; i < height; i++) {
			SetCursor(sx, sy + i);
			LCD_CMD_ADDR = ramcmd;
			for (uint16_t j = 0; j < width; j++) {
				LCD_DATA_ADDR = color[i * width + j];
			}
		}
	}
}

void TouchScreenLcd::DrawLine(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2) {
	uint16_t t;
	int xerr = 0, yerr = 0, delta_x, delta_y, distance;
	int incx, incy, uRow, uCol;
	delta_x = x2 - x1;
	delta_y = y2 - y1;
	uRow = x1;
	uCol = y1;
	if (delta_x > 0)
		incx = 1;
	else if (delta_x == 0)
		incx = 0;
	else {
		incx = -1;
		delta_x = -delta_x;
	}
	if (delta_y > 0)
		incy = 1;
	else if (delta_y == 0)
		incy = 0;
	else {
		incy = -1;
		delta_y = -delta_y;
	}
	if (delta_x > delta_y)
		distance = delta_x;
	else
		distance = delta_y;
	for (t = 0; t <= distance + 1; t++) {
		DrawPoint(uRow, uCol);
		xerr += delta_x;
		yerr += delta_y;
		if (xerr > distance) {
			xerr -= distance;
			uRow += incx;
		}
		if (yerr > distance) {
			yerr -= distance;
			uCol += incy;
		}
	}
}

void TouchScreenLcd::DrawRectangle(int16_t x1, int16_t y1, uint16_t x2, uint16_t y2) {
	DrawLine(x1, y1, x2, y1);
	DrawLine(x1, y1, x1, y2);
	DrawLine(x1, y2, x2, y2);
	DrawLine(x2, y1, x2, y2);
}

void TouchScreenLcd::DrawCircle(int16_t x0, int16_t y0, uint8_t r) {
	int a, b;
	int di;
	a = 0;
	b = r;
	di = 3 - (r << 1);             //Flag to indicate next point location
	while (a <= b) {
		DrawPoint(x0 + a, y0 - b);             //5
		DrawPoint(x0 + b, y0 - a);             //0
		DrawPoint(x0 + b, y0 + a);             //4
		DrawPoint(x0 + a, y0 + b);             //6
		DrawPoint(x0 - a, y0 + b);             //1
		DrawPoint(x0 - b, y0 + a);
		DrawPoint(x0 - a, y0 - b);             //2
		DrawPoint(x0 - b, y0 - a);             //7
		a++;
		//Use Bresenham algo to draw circle
		if (di < 0)
			di += 4 * a + 6;
		else {
			di += 10 + 4 * (a - b);
			b--;
		}
	}
}

void TouchScreenLcd::ShowChar(int16_t x, int16_t y, uint8_t num, uint8_t size, uint8_t mode) {
	uint8_t temp;
	int16_t x0 = x;
	int16_t tempx, tempy;
	uint16_t csize = (size / 16 + ((size % 16) ? 1 : 0)) * size;
	num -= ' ';
	uint16_t t = 0;
	if (y < upper_bound) {
		t = -(y - upper_bound) * (size / 16 + ((size % 16) ? 1 : 0));
		y = upper_bound;
	}
	if (x >= width || y >= height)
		return;
	if (!mode) {
		Set_Window(x < left_bound ? left_bound : x, y, x < left_bound ? size / 2 + x - left_bound : size / 2, size);
		LCD_CMD_ADDR = ramcmd;
	} else {
		tempx = x;
		tempy = y;
	}
	bool prev_is_true = false;
	for (; t < csize; t++) {
		if (size == 48)
			temp = asc2_4824[num][t];
		else
			return;
		for (uint8_t i = 0; i < 8; i++) {
			if (x >= width || y >= height) {
				Set_Window(0, 0, width, height);
				return;
			}
			if (x >= left_bound) {
				tempx = x;
				if (temp & 1 << i) {
					if (mode && !prev_is_true) {
						SetCursor(tempx, tempy);
						LCD_CMD_ADDR = ramcmd;
						prev_is_true = true;
					}
					tempx++;
					LCD_DATA_ADDR = POINT_COLOR;
				} else if (mode == 0) {
					LCD_DATA_ADDR = BACK_COLOR;
				} else {
					tempx++;
					prev_is_true = false;
				}
			}
			if (++x == x0 + size / 2) {
				y++;
				x = x0;
				if (mode != 0) {
					tempx = x;
					tempy = y;
					prev_is_true = false;
				}
				break;
			}
		}
	}
	Set_Window(0, 0, width, height);
}

uint32_t TouchScreenLcd::LCD_Pow(uint8_t m, uint8_t n) {
	uint32_t result = 1;
	while (n--)
		result *= m;
	return result;
}

void TouchScreenLcd::ShowNum(int16_t x, int16_t y, uint32_t num, uint8_t len, uint8_t size) {
	uint8_t t, temp;
	uint8_t enshow = 0;
	for (t = 0; t < len; t++) {
		temp = (num / LCD_Pow(10, len - t - 1)) % 10;
		if (enshow == 0 && t < (len - 1)) {
			if (temp == 0) {
				ShowChar(x + (size / 2) * t, y, ' ', size, 0);
				continue;
			} else
				enshow = 1;

		}
		ShowChar(x + (size / 2) * t, y, temp + '0', size, 0);
	}
}

void TouchScreenLcd::ShowxNum(int16_t x, int16_t y, uint32_t num, uint8_t len, uint8_t size, uint8_t mode) {
	uint8_t t, temp;
	uint8_t enshow = 0;
	for (t = 0; t < len; t++) {
		temp = (num / LCD_Pow(10, len - t - 1)) % 10;
		if (enshow == 0 && t < (len - 1)) {
			if (temp == 0) {
				if (mode & 0X80)
					ShowChar(x + (size / 2) * t, y, '0', size, mode & 0X01);
				else
					ShowChar(x + (size / 2) * t, y, ' ', size, mode & 0X01);
				continue;
			} else
				enshow = 1;

		}
		ShowChar(x + (size / 2) * t, y, temp + '0', size, mode & 0X01);
	}
}

void TouchScreenLcd::ShowString(int16_t x, int16_t y, uint16_t width, uint16_t height, uint8_t size, char *p, uint8_t mode) {
	if (mode) {
		int16_t x0 = x;
		width += x < 0 ? -x : x;
		height += y < 0 ? -y : y;
		while ((*p <= '~') && (*p >= ' '))	//check if valid
		{
			if (x >= width) {
				x = x0;
				y += size;
			}
			if (y >= height)
				break;	//exit
			ShowChar(x, y, *p, size, mode);
			x += size / 2;
			p++;
		}
	} else {
		uint16_t length = strlen(p);
		int16_t x0 = x;
		int16_t y0 = y;
		int16_t ex = width + std::abs(x);
		if (ex >= this->width)
			ex = this->width;
		if (ex >= length * (size / 2) + std::abs(x))
			ex = length * (size / 2) + std::abs(x);
		int16_t ey = height + std::abs(y);
		if (ey >= this->height)
			ey = this->height;
		x = x > left_bound ? x : left_bound;
		y = y > upper_bound ? y : upper_bound;
		width = ex - x;
		height = ey - y;
		Set_Window(x, y, width, height);
		LCD_CMD_ADDR = ramcmd;
		for (uint16_t i = y - y0; i < size; i++) {
			x = x0;
			for (uint16_t char_no = 0; char_no < length; char_no++) {
				uint16_t bound = (i + 1) * (size / 16 + ((size % 16) ? 1 : 0));
				uint16_t char_size = 0;
				for (uint16_t t = i * (size / 16 + ((size % 16) ? 1 : 0)); t < bound; t++) {
					uint8_t temp;
					if (size == 48)
						temp = asc2_4824[p[char_no] - ' '][t];
					else
						return;
					for (uint8_t bit = 0; bit < 8; bit++) {
						if (x >= left_bound && x < ex) {
							if (temp & 1 << bit)
								LCD_DATA_ADDR = POINT_COLOR;
							else
								LCD_DATA_ADDR = BACK_COLOR;
						}
						x++;
						if (++char_size >= size)
							break;
					}
				}
			}
			if (++y >= ey) {
				Set_Window(0, 0, this->width, this->height);
				return;
			}
			x = x0;
		}
		Set_Window(0, 0, this->width, this->height);
	}
}

